package models;

public class Customer {

	private String status;
	private String siteId;
	
	public String getStatus() {
		return status;
	}

	public String getSiteID() {
		return siteId;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public void setSiteID(String siteId) {
		this.siteId = siteId;
	}

}
